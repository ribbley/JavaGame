package General.Utility;

public class Vektor {
	private double x,y;
		public Vektor(double x, double y){
			this.x=x;
			this.y=y;
		}
		public void setX(double x){
			this.x = x;
		}
		public void setY(double y){
			this.y = y;
		}
		
		public double getX(){
			return x;
		}
		public double getY(){
			return y;
		}
}
